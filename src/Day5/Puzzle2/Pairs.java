package Day5.Puzzle2;

public class Pairs {

    private int firstState;
    private double secondState;

    public Pairs(int firstState, double secondState) {
        this.firstState = firstState;
        this.secondState = secondState;
    }

    public int getFirstState() {
        return firstState;
    }

    public void setFirstState(int firstState) {
        this.firstState = firstState;
    }

    public double getSecondState() {
        return secondState;
    }

    public void setSecondState(double secondState) {
        this.secondState = secondState;
    }
}
